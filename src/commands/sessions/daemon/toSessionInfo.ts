import type { Session, SessionInfo } from "./createSession";
import { originForCwd } from "./originForCwd";

export function toSessionInfo({
	id,
	name,
	title,
	subtitle,
	commandType,
	harness,
	status,
	startedAt,
	runningMs,
	runningSince,
	runName,
	runArgs,
	server,
	serverPort,
	serverOrigin,
	assistArgs,
	cwd,
	restored,
	error,
	activity,
	autoRun,
	autoAdvance,
	starred,
	usedPct,
	design,
	pendingPrPreview,
}: Session): SessionInfo {
	return {
		id,
		name,
		title,
		subtitle,
		commandType,
		harness,
		status,
		startedAt,
		runningMs,
		runningSince,
		runName,
		runArgs,
		server,
		port: serverPort,
		remoteOrigin: serverOrigin ?? originForCwd(cwd),
		assistArgs,
		cwd,
		restored,
		error,
		activity,
		autoRun,
		autoAdvance,
		starred,
		usedPct,
		design,
		pendingPrPreview,
	};
}
